    private void backBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backBtnActionPerformed
        // TODO add your handling code here:
        
        userProcessContainer.remove(this);
        Component[] componentArray = userProcessContainer.getComponents();
        Component component = componentArray[componentArray.length - 1];
        ManageCatalog manageCatalog = (ManageCatalog) component;
        manageCatalog.populateDrugTable();
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.previous(userProcessContainer);
    }//GEN-LAST:event_backBtnActionPerformed

    private void createBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createBtnActionPerformed
        // TODO add your handling code here:
        
        if(nameTextField.getText().trim().isEmpty())  {
            JOptionPane.showMessageDialog(this, "Drug Name cannot be empty!", "WARNING", JOptionPane.WARNING_MESSAGE);
            nameTextField.setBackground(Color.red);
        }
        else if (!nameTextField.getText().matches("[a-zA-Z ]*")){
            JOptionPane.showMessageDialog(this, "Please enter valid Drug Name!", "Error", JOptionPane.OK_OPTION);
            nameTextField.setBackground(Color.red);
        }
        else if(mrpTextField.getText().trim().isEmpty())  {
            JOptionPane.showMessageDialog(this, "Market Price cannot be empty!", "WARNING", JOptionPane.WARNING_MESSAGE);
            mrpTextField.setBackground(Color.red);
        }
        else if (!mrpTextField.getText().matches("[0-9]*")){
            JOptionPane.showMessageDialog(this, "Please enter valid Market Price!", "Error", JOptionPane.OK_OPTION);
            mrpTextField.setBackground(Color.red);
        }
        else if(listPriceTextField.getText().trim().isEmpty())  {
            JOptionPane.showMessageDialog(this, "List Price cannot be empty!", "WARNING", JOptionPane.WARNING_MESSAGE);
            listPriceTextField.setBackground(Color.red);
        }
        else if (!listPriceTextField.getText().matches("[0-9]*")){
            JOptionPane.showMessageDialog(this, "Please enter valid List Price!", "Error", JOptionPane.OK_OPTION);
            listPriceTextField.setBackground(Color.red);
        }
        else if(availTextField.getText().trim().isEmpty())  {
            JOptionPane.showMessageDialog(this, "Availability cannot be empty!", "WARNING", JOptionPane.WARNING_MESSAGE);
            availTextField.setBackground(Color.red);
        }
        else if (!availTextField.getText().matches("[0-9]*")){
            JOptionPane.showMessageDialog(this, "Please enter valid Availability!", "Error", JOptionPane.OK_OPTION);
            availTextField.setBackground(Color.red);
        }
        else if(availTextField.getText().equals("0"))  {
            JOptionPane.showMessageDialog(this, "Availability cannot be zero!", "WARNING", JOptionPane.WARNING_MESSAGE);
            availTextField.setBackground(Color.red);
        }
        else if(Integer.parseInt(mrpTextField.getText()) > Integer.parseInt(listPriceTextField.getText()))   {
                JOptionPane.showMessageDialog(this, "List Price cannot be less than MRP!", "WARNING", JOptionPane.WARNING_MESSAGE);
        }
        else    {
            Drug drug = supplier.getDrugCatalog().addDrug();

            drug.setDrugName(nameTextField.getText());
            drug.setPrice(Integer.parseInt(mrpTextField.getText()));
            drug.setListPrice(Integer.parseInt(listPriceTextField.getText()));
            drug.setAvail(Integer.parseInt(availTextField.getText()));
        
            JOptionPane.showMessageDialog(null, "Drug added!", "Info", JOptionPane.INFORMATION_MESSAGE);
            resetFields();
        }
    }//GEN-LAST:event_createBtnActionPerformed
    
    public void resetFields()   {
        nameTextField.setText("");
        nameTextField.setBackground(Color.white);
        mrpTextField.setText("");
        mrpTextField.setBackground(Color.white);
        listPriceTextField.setText("");
        listPriceTextField.setBackground(Color.white);
        availTextField.setText("");
        availTextField.setBackground(Color.white);
    }
    

