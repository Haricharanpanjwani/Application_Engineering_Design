    private void createBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createBtnActionPerformed
        // TODO add your handling code here:
        if(nameTextField.getText().trim().isEmpty())  {
            JOptionPane.showMessageDialog(this, "Store Name cannot be empty!", "WARNING", JOptionPane.WARNING_MESSAGE);
            nameTextField.setBackground(Color.red);
        }
        else if (!nameTextField.getText().matches("[a-zA-Z ]*")){
            JOptionPane.showMessageDialog(this, "Please enter valid Store Name!", "Error", JOptionPane.OK_OPTION);
            nameTextField.setBackground(Color.red);
        }
        else if(locationTextField.getText().trim().isEmpty())  {
            JOptionPane.showMessageDialog(this, "Store Location cannot be empty!", "WARNING", JOptionPane.WARNING_MESSAGE);
            locationTextField.setBackground(Color.red);
        }
        else if (!locationTextField.getText().matches("[a-zA-Z ]*")){
            JOptionPane.showMessageDialog(this, "Please enter valid Store Location!", "Error", JOptionPane.OK_OPTION);
            locationTextField.setBackground(Color.red);
        }
        else if(streetTextField.getText().trim().isEmpty())  {
            JOptionPane.showMessageDialog(this, "Store Street cannot be empty!", "WARNING", JOptionPane.WARNING_MESSAGE);
            streetTextField.setBackground(Color.red);
        }
        else if (!streetTextField.getText().matches("[a-zA-Z ]*")){
            JOptionPane.showMessageDialog(this, "Please enter valid Store Street!", "Error", JOptionPane.OK_OPTION);
            streetTextField.setBackground(Color.red);
        }
        else if(cityTextField.getText().trim().isEmpty())  {
            JOptionPane.showMessageDialog(this, "Store City cannot be empty!", "WARNING", JOptionPane.WARNING_MESSAGE);
            cityTextField.setBackground(Color.red);
        }
        else if (!cityTextField.getText().matches("[a-zA-Z ]*")){
            JOptionPane.showMessageDialog(this, "Please enter valid Store City!", "Error", JOptionPane.OK_OPTION);
            cityTextField.setBackground(Color.red);
        }
        else    {
            store = storeCatalog.addStore();
            store.setStoreName(nameTextField.getText());
            store.setLocation(locationTextField.getText());
            store.setStreet(streetTextField.getText());
            store.setCity(cityTextField.getText());
            JOptionPane.showMessageDialog(this, "Store is successfully created!", "INFORMATION", JOptionPane.INFORMATION_MESSAGE);
            
            resetFields();
        }
    }//GEN-LAST:event_createBtnActionPerformed
    
    public void resetFields()   {
        nameTextField.setText("");
        nameTextField.setBackground(Color.white);
        locationTextField.setText("");
        locationTextField.setBackground(Color.white);
        streetTextField.setText("");
        streetTextField.setBackground(Color.white);
        cityTextField.setText("");
        cityTextField.setBackground(Color.white);
    }

