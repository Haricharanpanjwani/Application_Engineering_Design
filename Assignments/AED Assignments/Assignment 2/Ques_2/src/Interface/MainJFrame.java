    private void createBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createBtnActionPerformed
        // TODO add your handling code here:
        age = profile.getAge();
        
        if(age == 0)    {
            CreatePatient createPatient = new CreatePatient(profile);
            splitPane.setRightComponent(createPatient);
        }
        else    {
            JOptionPane.showMessageDialog(this, "Patient details are already saved!!", "Information", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_createBtnActionPerformed

