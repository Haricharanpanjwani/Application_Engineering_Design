    private void populateTable() {
        
        DefaultTableModel dtm = (DefaultTableModel)vitalSignTable.getModel();
        
        //Clear the table before displaying anything
        dtm.setRowCount(0);
        
        //Clear the table before displaying anything  
    /*  int rowCount = vitalSignTable.getRowCount();
        for(int i = rowCount; i >= 0; i--)  {
            dtm.removeRow(i);
        }   */
        
        //Adding the rows to the vitalSignTable
        for(VitalSign vs : vitalSignHistory.getVitalSignList()) {
            
            Object row[] = new Object[4];
            
            row[0] = vs.getTemperature();
            row[1] = vs.getBloodPressure();    
            row[2] = vs.getPulse();
            row[3] = vs;
            
            dtm.addRow(row);
        }   
    }

