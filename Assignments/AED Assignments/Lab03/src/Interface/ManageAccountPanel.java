    public void populateTable() {
        
        //Get the Table Model
        DefaultTableModel dtm = (DefaultTableModel)accountTable.getModel();
        
        dtm.setRowCount(0);
        
        for(Account a : accountList.getAccountList())   {   
            Object row[] = new Object[4];
            
            row[0] = a;
            row[1] = a.getRoutingNum();
            row[2] = a.getAccountNum();
            row[3] = a.getBalance();
            
            //add each account to the table for dispalay
            dtm.addRow(row);
        }
    }

