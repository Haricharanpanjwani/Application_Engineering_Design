    private void viewBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewBtnActionPerformed
        // TODO add your handling code here:
        
        int selectedRow = accountTable.getSelectedRow();
        
        if(selectedRow >= 0)    {
            //get the selected row
            Account account = (Account)accountTable.getValueAt(selectedRow, 0);
            
            ViewAccountPanel view = new ViewAccountPanel(account, userProcessContainer, this);
            userProcessContainer.add("ViewAccountPanel",view);
            CardLayout layout = (CardLayout)userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
        else    {
            JOptionPane.showMessageDialog(this, "Please select a row!", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_viewBtnActionPerformed

    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
        
        //get the selected row
        int selectedRow = accountTable.getSelectedRow();
        
        if(selectedRow >= 0)    {
            
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this row ?", "Warning", dialogButton);
            if(dialogResult == JOptionPane.YES_OPTION)  {

                Account acc = (Account)accountTable.getValueAt(selectedRow, 0);
                accountList.deleteAccount(acc);
                JOptionPane.showMessageDialog(this, "Account has been deleted", "Information", JOptionPane.INFORMATION_MESSAGE);                

                //Populate the new Table after deleting the account
                populateTable();
            }
        }
        else    {
            JOptionPane.showMessageDialog(this, "Please select a row!", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        // TODO add your handling code here:
        
        //get the input
        String accountNum = searchTextField.getText();
        
        //search the account
        Account account = accountList.searchAccount(accountNum);
        
        if(account == null) {
            //Display the message that account not found
            return;
        }else   {
            //display the account details on search results
            SearchResultPanel searchPanel = new SearchResultPanel(account, userProcessContainer);
            userProcessContainer.add("searchPanel",searchPanel);
            CardLayout layout = (CardLayout)userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
    }//GEN-LAST:event_searchBtnActionPerformed


