    private void createBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createBtnActionPerformed
        // TODO add your handling code here:
        
        String routingNum = routingNumTextField.getText();
        String accountNum = accountNumTextField.getText();
        String bankName = bankNameTextField.getText();
        String balance = balanceTextField.getText();
        
        Account account = new Account();
        
        account.setRoutingNum(routingNumTextField.getText());
        account.setAccountNum(accountNumTextField.getText());
        account.setBankName(bankNameTextField.getText());
        account.setBalance(balanceTextField.getText());
        
        accountList.addAccount(account);
        
        JOptionPane.showMessageDialog(this, "The Account has been successfully added to the Account Directory.", "Information", JOptionPane.INFORMATION_MESSAGE);
        
        resetFields(); 
    }//GEN-LAST:event_createBtnActionPerformed
    
