    private void submitJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitJButtonActionPerformed
        
        int size = donor.getDonationList().size() - 1;
              
        request.setTestResult(resultJTextField.getText());        
        request.setStatus("Completed");
        request.setResolveDate(new Date());
        
        Donation d  = donor.getDonationList().get(size);
        d.setBarCode(resultJTextField.getText());
        d.setUnit(Integer.parseInt(unitTextField.getText()));
        d.setNurse(request.getReceiver().getUsername());
        d.setResolveDate(request.getResolveDate());
        
        JOptionPane.showMessageDialog(this, "Result has been updated for the Donor!!", "INFORMATION", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_submitJButtonActionPerformed


