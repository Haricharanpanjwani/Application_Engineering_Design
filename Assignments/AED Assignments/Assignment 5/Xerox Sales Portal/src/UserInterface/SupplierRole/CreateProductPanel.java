    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        
        if(txtName.getText().trim().isEmpty())  {
            JOptionPane.showMessageDialog(this, "Name cannot be empty!!", "Info", JOptionPane.INFORMATION_MESSAGE);
            txtName.setBackground(Color.red);
        }   else if(availTextField.getText().trim().isEmpty())    {
            JOptionPane.showMessageDialog(this, "Availability cannot be empty!!", "Info", JOptionPane.INFORMATION_MESSAGE);
            availTextField.setBackground(Color.red);
        }   else if(floorPriceTextField.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Price cannot be empty!!", "Info", JOptionPane.INFORMATION_MESSAGE);
            floorPriceTextField.setBackground(Color.red);
        }   else {
            Product p = supplier.getProductCatalog().addProduct();

            p.setProdName(txtName.getText());
            p.setAvail(Integer.parseInt(availTextField.getText()));
            p.setFloorPrice(Integer.parseInt(floorPriceTextField.getText()));
            p.setCeilingPrice();
            p.setTargetPrice();

            JOptionPane.showMessageDialog(this, "Product added!", "Info", JOptionPane.INFORMATION_MESSAGE);
            
            txtName.setText("");
            availTextField.setText("");
            floorPriceTextField.setText("");
            
            txtName.setBackground(Color.white);
            availTextField.setBackground(Color.white);
            floorPriceTextField.setBackground(Color.white);
        }
    }//GEN-LAST:event_btnAddActionPerformed

