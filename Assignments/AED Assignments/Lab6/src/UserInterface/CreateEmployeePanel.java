    private void backBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backBtnActionPerformed
        // TODO add your handling code here:
        userProcessContainer.remove(this);
        
        Component[] componentArray = userProcessContainer.getComponents();
        Component component = componentArray[componentArray.length - 1];
        
        ManageEmployeePanel empPanel = (ManageEmployeePanel) component;
        empPanel.populateTable();
        
        CardLayout layout = (CardLayout)userProcessContainer.getLayout();
        layout.previous(userProcessContainer);
    }//GEN-LAST:event_backBtnActionPerformed

    private void createBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createBtnActionPerformed
        // TODO add your handling code here:
        
        if(firstNameTextField.getText().trim().isEmpty() || lastNameTextField.getText().trim().isEmpty() ||
             organizationTextField.getText().trim().isEmpty())  {
            
            JOptionPane.showMessageDialog(this, "Fields cannot be empty!", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
        else    {
            Employee emp = employeeDirectory.addEmployee();
        
            emp.setFirstName(firstNameTextField.getText());
            emp.setLastName(lastNameTextField.getText());
            emp.setOrganization(organizationTextField.getText());

            JOptionPane.showMessageDialog(this, "Employee is added successfully!", "INFORMATION", JOptionPane.INFORMATION_MESSAGE);
            resetFields();
        }
        
    }//GEN-LAST:event_createBtnActionPerformed
    
    public void resetFields()   {
        firstNameTextField.setText("");
        lastNameTextField.setText("");
        organizationTextField.setText("");
    }

