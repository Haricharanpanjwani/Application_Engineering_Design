package com.my.finalproject.model;

public class PainCategory {

	
	private int painid;
	private String painname;
	private String des;
	
	public int getPainid() {
		return painid;
	}
	public void setPainid(int painid) {
		this.painid = painid;
	}
	public String getPainname() {
		return painname;
	}
	public void setPainname(String painname) {
		this.painname = painname;
	}
	public String getDes() {
		return des;
	}
	public void setDes(String des) {
		this.des = des;
	}
	
	
}
