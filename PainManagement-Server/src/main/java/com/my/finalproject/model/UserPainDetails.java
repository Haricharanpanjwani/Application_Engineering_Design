package com.my.finalproject.model;

public class UserPainDetails {

	private int userid;
	private int painid;
	private String date;
	
	public int getUserid() {
		return userid;
	}
	public void setUserid(int userid) {
		this.userid = userid;
	}
	public int getPainid() {
		return painid;
	}
	public void setPainid(int painid) {
		this.painid = painid;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	
	
}
